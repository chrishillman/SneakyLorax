def getcolors(side):
    """
    get a list of colors prepended with either fg or bg
    :param side: string, fg or bg
    :return: list of tuples for SelectField (value, display)
    """
    choices = []
    for color in colorslist:
        choices.append((f"{side}{color}", color))
    return choices